CREATE TABLE DEPT_INFO (
    DEPTID   VARCHAR2(4),
    DEPTNAME VARCHAR2(25),
    CONSTRAINT PK_DEPT_INFO PRIMARY KEY ( DEPTID )
);

CREATE TABLE EMP_INFO (
    EMPID   VARCHAR2(5),
    EMPNAME VARCHAR2(25),
    DEPTID  VARCHAR2(4),
    CONSTRAINT PK_EMP_INFO PRIMARY KEY ( EMPID ),
    CONSTRAINT FK_EMP_INFO FOREIGN KEY ( DEPTID )
        REFERENCES DEPT_INFO ( DEPTID )
);

CREATE TABLE EMP_PAY_INFO (
    EMPID    VARCHAR2(5),
    "_MONTH" VARCHAR2(12),
    "_YEAR"  NUMBER,
    NETPAY   NUMBER,
    CONSTRAINT PK_EMP_PAY_INFO PRIMARY KEY ( EMPID,
                                             "_MONTH",
                                             "_YEAR" ),
    CONSTRAINT FK_EMP_PAY_INFO FOREIGN KEY ( EMPID )
        REFERENCES EMP_INFO ( EMPID )
);
